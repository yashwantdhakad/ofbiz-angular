/**
 * Decodes a base64 string (as returned by OFBiz REST byte[] OUT params)
 * into a Blob. Returns an empty Blob when the payload is missing so
 * callers can hand the result straight to URL.createObjectURL.
 */
export function base64ToBlob(base64: string | null | undefined, mimeType: string): Blob {
  if (!base64) {
    return new Blob([new Uint8Array()], { type: mimeType });
  }
  const binaryString = window.atob(base64);
  const len = binaryString.length;
  const bytes = new Uint8Array(len);
  for (let i = 0; i < len; i++) {
    bytes[i] = binaryString.charCodeAt(i);
  }
  return new Blob([bytes], { type: mimeType });
}
